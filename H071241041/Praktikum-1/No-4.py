Suhu = int(input("masukkan suhu: "))
Celcius = Suhu 
Kelvin = Celcius + 273
Reamur = Celcius * 4/5
Fahrenheit = Celcius * 9/5 + 32
print ("Suhu Celcius", Celcius, "sama dengan", Kelvin,"Kelvin")
print ("Suhu Celcius", Celcius, "sama dengan", Reamur,"Reamur")
print ("Suhu Celcius", Celcius, "sama dengan", Fahrenheit,"Fahrenheit")