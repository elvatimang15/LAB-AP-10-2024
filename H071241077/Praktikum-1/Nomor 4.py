celcius = float(input("masukkan suhu dalam celcius : "))
kelvin = celcius + 273.15
reamur = 4/5 * celcius
farenheit = (9/5 * celcius) + 32

print(f"hasil konversi suhu dari suhu {celcius} derajat ke kelvin adalah {kelvin}K")
print(f"hasil konversi suhu dari suhu {celcius} derajat ke reamur adalah {reamur}R")
print(f"hasil konversi suhu dari suhu {celcius} derajat ke farenheit adalah {farenheit}F")